// Función para obtener el color de una categoría
export const getCategoryColor = (categoryId: string): string => {
  const category = componentCategories.find(cat => cat.id === categoryId);
  return category?.color || "from-gray-500 to-gray-600";
};

// Función para obtener todas las categorías
export const getAllCategories = (): CategoryConfig[] => {
  return componentCategories;
};

// Función para obtener categorías únicas de una lista de componentes
export const getUniqueCategories = (components: any[]): CategoryConfig[] => {
  const categoryIds = Array.from(
    new Set(
      components
        .map((comp) => comp.category)
        .filter(Boolean) as string[]
    )
  );
  
  return categoryIds
    .map(id => componentCategories.find(cat => cat.id === id))
    .filter(Boolean) as CategoryConfig[];
}; 